const kalimat = ["halo", "nama", "saya", "khalid"];

console.log(kalimat.join()); // halo,nama,saya,khalid
console.log(kalimat.join("")); // halonamasyakhalid
console.log(kalimat.join(" ")); // halo nama saya khalid
console.log(kalimat.join(" - ")); // halo - nama - saya - khalid
console.log(kalimat.join(" / ")); // halo / nama / saya / khalid
